package it.polito.softeng.sort;

/**
 * Basic abstract class for the {@code int} array sorting algorithms used in the power tracing experiments
 * 
 * @author mtk
 *
 */
public abstract class IntSortAlgorithm {
	
	/**
	 * This method shall contain the implementation of the sorting algorithm.
	 * 
	 * @param a the array of {@code int} to be cloned
	 */
	public abstract void sort(int[] a);
	

}
